    <li>Clothes shopping</li>
    <li>Eating out</li>
</ul>

<h2>Plan a monthly budget</h2>
<p>So, you've worked out why you want to budget, what you want to save for, and what your fixed and variable costs are. Now it's time to work out how to save money each month.</p>
<p>The 50/30/20 rule encourages your budget to look as follows:</p>
<ul>
    <li>50% of your income goes towards your “needs,” i.e. your fixed costs such as rent and bills.</li>
    <li>30% is allocated to your “wants,” i.e. your variable costs such as eating out, trips to the hairdresser and clothes shopping.</li>
    <li>20% goes into your savings or towards paying off debt.
</li><br>

<p>The 50/30/20 rule was actually created by US senator Elizabeth Warren, a bankruptcy specialist at Harvard, as a way to show American citizens how to budget and save money and how to budget money on a low income.</p>
