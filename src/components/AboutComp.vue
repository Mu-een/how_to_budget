<h2>Don't ask how to budget money, ask why you want to budget</h2>
<p>The key to success in any endeavor is to create specific, yet challenging objectives. Understanding what motivates you to save can go a long way toward creating clear, achievable savings targets. And keeping your savings goals in mind can help you to stay focused and on track, even when things aren't so easy.</p>

<h2>Distinguish between short-term savings goals and long-term saving goals</h2>
<p>What is a short-term savings goal? These relatively modest targets may include the following:</p>
<ul>
    <li>A nice piece of furniture</li>
    <li>A weekend vacation</li>
    <li>A down payment on a car</li>
    <li>An emergency fund</li>
</ul>

<p>Long-term savings goals might look like:</p>
<ul>
    <li>A deposit for a flat or a house</li>
    <li>Paying off any long-standing debt</li>
    <li>Starting up your own business</li>
    <li>A trip around the world</li>
    <li>Saving for retirement</li>
</ul>
<p>Having a combination of both short- and long-term saving goals can make the larger goals seem less intimidating. As you gradually achieve your short-term goals, you may realize how possible it is to save money and be in control of your finances. And this can make your long-term goals feel increasingly attainable.</p>

<h2>Track your spending to create a solid budget</h2>
<p>If you want to start budgeting effectively, you need to understand exactly how much is coming in and out of your account every month.
The best way to do this is to track all of your income and expenses over a 30-day period. This means being aware of every single transaction and either noting it down in a spreadsheet or using our budgeting app. </p>

<h2>Separate fixed expenses from variable expenses</h2>
<p>The category of fixed costs may include:</p>
<ul>
    <li>Monthly rent</li>
    <li>Heating and electricity bills</li>
    <li>Insurance costs (i.e. car, personal liability or property)</li>
    <li>Student loan repayments</li>
</ul>

<p>Variable costs, which may change monthly or even weekly, include:</p>
<ul>
